#ifndef FLYCAM_HPP
#define FLYCAM_HPP


/////////////////////////////////////////////////////////////////////////////
// INCLUDES
/////////////////////////////////////////////////////////////////////////////
#include "Q_View.hpp"

/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
// FREEFLY CAMERA
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////
class flycam : public view
{
/////////////////////////////////////////////////////////////////////////////
public:
        flycam( void );
        flycam( f32 NearZ, f32 FarZ );

/////////////////////////////////////////////////////////////////////////////
public:

    void SetPosition        ( const vector3& Pos );
    void GetPosition        ( vector3& Pos );
    
    void MoveUp             ( f32 Offset );
    void MoveForward        ( f32 Offset );
    void MoveRight          ( f32 Offset );

    void AddYaw             ( radian Yaw   );
    void AddPitch           ( radian Pitch );
    void AddRoll            ( radian Roll  );

    void SetYaw             ( radian Yaw   );
    void GetYaw             ( radian& Yaw   );
    void SetPitch           ( radian Pitch );
    void GetPitch           ( radian& Pitch );
    void SetRoll            ( radian Roll  );
    void GetRoll            ( radian& Roll  );

    void LookAtPoint        ( const vector3& Target );

    void SetRotations       ( const radian3& Rot );
    void GetRotations       ( radian3& Rot     );

    void operator=          ( view& Src  ); 
    
/////////////////////////////////////////////////////////////////////////////
protected:

    vector3         m_Pos;
    radian          m_Pitch;
    radian          m_Yaw; 
    radian          m_Roll;
};

#endif