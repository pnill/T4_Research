void FONT_RenderText ( s32 x, s32 y, char* s, xbool Large );
void FONT_EndRender  ( void );

s32 FONT_GetStringWidth    ( char* Str );
s32 FONT_GetLineHeight     ( void );
s32 FONT_GetStringLineCount( char* Str );

